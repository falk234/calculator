// multiply
// divide

const add = (a,b) => {
    return a+b;
}

const subtract = (a,b) => {
    return a-b;
}

const multiply = (a,b) => {
    return a*b;
}

const divide = (a,b) => {
    return a/b;
}

let a=2;
let b=3;

console.log(add(a,b)+"=> 5");
console.log(subtract(a,b)+"=> -1");
console.log(multiply(a,b)+"=> 6");
console.log(divide(a,b)+" => 0,6");